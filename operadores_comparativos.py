##Operadores de Comparação

saldo = 450
saque = 200
print(saldo == saque)  #operador '==' quer dizer igual
print(saldo != saque)  #operador '!=' quer dizer diferente
print(saldo > saque)  #operador '>' quer dizer maior que
print(saldo < saque)  #operador '<' quer dizer menor que
print(saldo >= saque)  #operador '>=' quer dizer maior ou igual